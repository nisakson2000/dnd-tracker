/**
 * Find a spell by its exact name (case-insensitive).
 * @param {string} name - The spell name to search for.
 * @returns {object|undefined} The spell object or undefined if not found.
 */
export function getSpell(name) {
  const lower = name.toLowerCase();
  return SPELL_DATABASE.find(s => s.name.toLowerCase() === lower);
}

/**
 * Get all spells of a given level.
 * @param {number} level - The spell level (0 for cantrips).
 * @returns {object[]} Array of matching spell objects.
 */
export function getSpellsByLevel(level) {
  return SPELL_DATABASE.filter(s => s.level === level);
}

/**
 * Get all spells available to a given class.
 * @param {string} className - The class name (e.g. "Wizard").
 * @returns {object[]} Array of matching spell objects.
 */
export function getSpellsByClass(className) {
  const lower = className.toLowerCase();
  return SPELL_DATABASE.filter(s =>
    s.classes.some(c => c.toLowerCase() === lower)
  );
}

/**
 * Get all spells of a given school of magic.
 * @param {string} school - The school name (e.g. "Evocation").
 * @returns {object[]} Array of matching spell objects.
 */
export function getSpellsBySchool(school) {
  const lower = school.toLowerCase();
  return SPELL_DATABASE.filter(s => s.school.toLowerCase() === lower);
}

/**
 * Search spells by a text query (matches against name, school, description, and classes).
 * @param {string} query - The search string.
 * @returns {object[]} Array of matching spell objects.
 */
export function searchSpells(query) {
  const lower = query.toLowerCase();
  return SPELL_DATABASE.filter(s =>
    s.name.toLowerCase().includes(lower) ||
    s.school.toLowerCase().includes(lower) ||
    s.description.toLowerCase().includes(lower) ||
    s.classes.some(c => c.toLowerCase().includes(lower))
  );
}
